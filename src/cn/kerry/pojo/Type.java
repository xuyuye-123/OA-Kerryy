package cn.kerry.pojo;
/*
 * 类型实体类
 */
public class Type {
	private Integer id;//类型id
	private String name;//类型名称
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
